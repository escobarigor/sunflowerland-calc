/* ---------- Controle de idioma ---------- */
const Lang = {
  STORAGE_KEY: "sfl_eco_lang",
  current: "pt",

  init() {
    const saved = localStorage.getItem(this.STORAGE_KEY);
    this.current = (saved && I18N[saved]) ? saved : "pt";
  },
  set(lang) {
    if (!I18N[lang]) return;
    this.current = lang;
    localStorage.setItem(this.STORAGE_KEY, lang);
  },
  /* locale para formatar datas/horas */
  locale() {
    return { pt: "pt-BR", en: "en-US", es: "es-ES" }[this.current] || "pt-BR";
  }
};

/* ---------- t(): traduz uma chave ----------
   t("crops.best", { name: "X", value: "1" }) substitui {name} e {value}. */
function t(key, params) {
  const dict = I18N[Lang.current] || I18N.pt;
  let str = dict[key] || I18N.pt[key] || key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      str = str.split("{" + k + "}").join(v);
    }
  }
  return str;
}
